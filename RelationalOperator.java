public class RelationalOperator {
    public static void main(String[] args) {
        // Relational operator == ,<=,>=, !=,>,<
          int num1 = 20;
            int num2 = 30;
            System.out.println("Result = " + (num1 == num2));//false
            System.out.println("Result = " + (num1 <= num2));//20 <= 30 =true
            System.out.println("Result = " + (num1 >= num2));//20 >=30 =false
            System.out.println("Result = " + (num1 != num2));//20 !=30 = true
            System.out.println("Result = " + (num1 < num2));// 20 < 30 = Ture
    }
}

